package in.easysystems.studentadmission.util;

public class StudentQualifications 
{
	public float studTenthPercentage, studTwelfthPercentage, studGraduationPercentage, studPostGraduationPercentage;

	public StudentQualifications(	float studTenthPercentage,
									float studTwelfthPercentage, 
									float studGraduationPercentage,
									float studPostGraduationPercentage ) 
	{
		this.studTenthPercentage = studTenthPercentage;
		this.studTwelfthPercentage = studTwelfthPercentage;
		this.studGraduationPercentage = studGraduationPercentage;
		this.studPostGraduationPercentage = studPostGraduationPercentage;
	}
	
	

}
